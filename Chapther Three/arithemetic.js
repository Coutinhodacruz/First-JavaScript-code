let firstNumber = parseInt(prompt("Enter firstNumber "))
let secondNumber = parseInt(prompt("Enter secondNumber "))

let squareNumber = firstNumber ** 2;
let squareNumber2 = secondNumber ** 2;


let sum = squareNumber + squareNumber2;
let differenceNumber = squareNumber - squareNumber2;

console.log(`Square of first number: ${squareNumber}`);
console.log(`Square of second number: ${squareNumber2}`);
console.log(`Sum of squares: ${sum}`);
console.log(`Difference of squares: ${differenceNumber}`);